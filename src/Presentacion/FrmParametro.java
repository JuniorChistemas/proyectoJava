    private String Agregar()throws Exception{
        String Mensaje;
      txtIdParametro.setText(this.Codigo());
        Mensaje = Bo.Agregar(txtIdParametro.getText(),cbxTipo.getSelectedItem().toString(), txtDescripcion.getText(),this.Estado(jcbEstado.isSelected()));
        return Mensaje;
    }
    private String Estado(boolean obj){
        if(obj){
            return "Activo";
        }
        return "Inactivo";
    }
    private void Listar() {  //venta
        dtm = (DefaultTableModel) tbParametro.getModel();
        Object[] obj = new Object[4];
        for (ParametroDTO a : Bo.listar()) {
            obj[0] = a.getIdParamero().trim();
            obj[1] = a.getDescripcion().trim();
            obj[2] = a.getTipo().trim();
            obj[3] = a.getEstadoText().trim();
            dtm.addRow(obj);
        }
    }
     private void Categoria() {  //venta
        dtm = (DefaultTableModel) tbParametro.getModel();
        Object[] obj = new Object[4];
        for (ParametroDTO a : Bo.listarCategoria()) {
            obj[0] = a.getIdParamero().trim();
            obj[1] = a.getDescripcion().trim();
            obj[2] = a.getTipo().trim();
            obj[3] = a.getEstadoText().trim();
            dtm.addRow(obj);
        }
    }
      private void Marca() {  //venta
        dtm = (DefaultTableModel) tbParametro.getModel();
        Object[] obj = new Object[4];
        for (ParametroDTO a : Bo.listarMarca()) {
            obj[0] = a.getIdParamero().trim();
            obj[1] = a.getDescripcion().trim();
            obj[2] = a.getTipo().trim();
            obj[3] = a.getEstadoText().trim();
            dtm.addRow(obj);
        }
    }

    private String Codigo() {
        return Bo.CodigoParametro();
    }
    private void limpiarTabla() {
        dtm = (DefaultTableModel) tbParametro.getModel();
        dtm.getDataVector().removeAllElements();
        tbParametro.removeAll();
    }
    private String Actualizar()throws Exception{
        String Mensaje;
        Mensaje = Bo.Actualizar(txtIdParametro.getText().trim(),cbxTipo.getSelectedItem().toString(), txtDescripcion.getText().trim(),this.Estado(jcbEstado.isSelected()));
        return Mensaje;
    }
    private String Eliminar()throws Exception{
        String Mensaje;
        Mensaje= Bo.Eliminar(txtIdParametro.getText().trim());
        return Mensaje;
    }
    
