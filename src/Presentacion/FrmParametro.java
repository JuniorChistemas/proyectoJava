    private void tbParametroMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbParametroMousePressed
      int fila = tbParametro.getSelectedRow();
      if(fila ==-1){
          JOptionPane.showMessageDialog(null, "seleccionar correctamente");
      }else{
          txtIdParametro.setText(tbParametro.getValueAt(fila, 0).toString());
          cbxTipo.setSelectedItem(tbParametro.getValueAt(fila, 1).toString().trim());
          txtDescripcion.setText(tbParametro.getValueAt(fila, 2).toString());
          jcbEstado.setSelected(tbParametro.getValueAt(fila, 3).equals("Activo"));
      }
      
    }//GEN-LAST:event_tbParametroMousePressed

